        public int No { get; set; }

        public override bool Equals(object obj)
        {
            if (obj is IpPort po)
            {
                return No.Equals(po.No);
            }
            else if (obj is int i)
            {
                return No.Equals(i);
            }
            else
            {
                return false;
            }
        }
        public override int GetHashCode()
        {
            return No;
        }

        public override string ToString()
        {
            return $"{No}({GetName(No)})";
        }

        /// <summary>
        /// find port name
        /// </summary>
        /// <param name="portno"></param>
        /// <param name="def"></param>
        /// <returns></returns>
        public static string GetName(int portno, string def = "?")
        {
            if (portTable.TryGetValue(portno, out string name))
            {
                return name;
            }
            else
            {
                return def;
            }
        }
        /// <summary>
        /// find port name
        /// </summary>
        /// <param name="portnostr"></param>
        /// <param name="def"></param>
        /// <returns></returns>
        public static string GetName(string portnostr, string def = "?")
        {
            if (int.TryParse(portnostr, out int portno))
            {
                return GetName(portno, def);
            }
            else
            {
                return def;
            }
        }

